// Apply translations
function applyLang(lang) {
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = i18n[lang][key];
            } else if (el.tagName === 'META') {
                el.setAttribute('content', i18n[lang][key]);
            } else {
                el.innerHTML = i18n[lang][key];
            }
        }
    });
    
    // Toggle RTL-specific CSS class on body
    document.body.classList.toggle('rtl', lang === 'ar');
}

// Language toggle
function toggleLang() {
    const current = document.documentElement.lang === 'ar' ? 'en' : 'ar';
    localStorage.setItem('lang', current);
    applyLang(current);
    
    // Update toggle button text
    const btns = document.querySelectorAll('[data-lang-toggle]');
    btns.forEach(btn => {
        btn.textContent = current === 'ar' ? 'English' : 'عربي';
    });
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('lang') || 'en';
    applyLang(saved);
    
    // Set toggle button text
    document.querySelectorAll('[data-lang-toggle]').forEach(btn => {
        btn.textContent = saved === 'ar' ? 'English' : 'عربي';
        btn.addEventListener('click', toggleLang);
    });
});
